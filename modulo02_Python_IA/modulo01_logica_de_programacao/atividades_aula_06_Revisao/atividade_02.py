# Cálculo de Área:
# Crie um programa que calcule a área de um retângulo.
# Peça ao usuário para digitar a largura e a altura,
# armazene em variáveis e calcule a área.

largura = float(input('Digite a largura do rentangulo: '))
altura = float(input('Digite a altura do rentangulo: '))

area = largura * altura

print(f'A área possuí {area:.2f} M²')